/**
 * @author spoojar
 *
 */
public class APIConfigException extends APITestException {
	private static final long serialVersionUID = 1947606591597813059L;

	/**
	   * 
	   */
	public APIConfigException() {

	}

	/**
	 * @param arg0
	 */
	public APIConfigException(String arg0) {
		super(arg0);

	}

	/**
	 * @param arg0
	 */
	public APIConfigException(Throwable arg0) {
		super(arg0);

	}

	/**
	 * @param arg0
	 * @param arg1
	 */
	public APIConfigException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

}
